from legged_gym import LEGGED_GYM_ROOT_DIR
import os

import isaacgym
from legged_gym.envs import *
from legged_gym.utils import  get_args, export_policy_as_jit, task_registry, Logger

import numpy as np
import torch, json
from tqdm import tqdm

mini_prev = 0

def env_not_finished(env, pbar):
    global mini_prev
    if mini_prev != min_total_evaluations(env):
        mini_prev = min_total_evaluations(env)
        pbar.update()

    return mini_prev< env.cfg.logger.number_evaluations


def min_total_evaluations(env):
    mini = float("inf")
    for i in range(env.terrain_solved.shape[0]):
        mini = min(mini, env.info[i]["total"])
    return mini

def log_data(env):
    fname = env.cfg.terrain.terrain_type+"-"+str(env.cfg.terrain.terrain_direction_up)+"-"+str(env.cfg.logger.linear_vel)+"-"+str(env.cfg.logger.perpendicular_vel_max)+"-"+str(env.cfg.logger.vel_x)
    with open(os.path.join(LEGGED_GYM_ROOT_DIR, 'legged_gym', 'scripts',fname+'.json'), 'w') as json_file:
        json.dump(env.info, json_file, indent=4)

def play(args):
    env_cfg, train_cfg = task_registry.get_cfgs(name=args.task)
    # override some parameters for testing
    env_cfg.env.num_envs = int(env_cfg.terrain.num_rows*env_cfg.terrain.num_cols)
    env_cfg.terrain.curriculum = False
    env_cfg.noise.add_noise = False
    env_cfg.domain_rand.randomize_friction = False
    env_cfg.domain_rand.push_robots = False

    # prepare environment
    env, _ = task_registry.make_env(name=args.task, args=args, env_cfg=env_cfg)
    obs = env.get_observations()
    # load policy
    train_cfg.runner.resume = True
    ppo_runner, train_cfg = task_registry.make_alg_runner(env=env, name=args.task, args=args, train_cfg=train_cfg)
    policy = ppo_runner.get_inference_policy(device=env.device)

    logger = Logger(env.dt)
    robot_index = 0 # which robot is used for logging
    joint_index = 1 # which joint is used for logging
    stop_state_log = 100 # number of steps before plotting states
    stop_rew_log = env.max_episode_length + 1 # number of steps before print average episode rewards
    i=0; pbar = tqdm(total=env.cfg.logger.number_evaluations)

    while env_not_finished(env, pbar):
        actions = policy(obs.detach())
        obs, _, rews, dones, infos = env.step(actions.detach())

        if i < stop_state_log:
            logger.log_states(
                {
                    'dof_pos_target': actions[robot_index, joint_index].item() * env.cfg.control.action_scale,
                    'dof_pos': env.dof_pos[robot_index, joint_index].item(),
                    'dof_vel': env.dof_vel[robot_index, joint_index].item(),
                    'dof_torque': env.torques[robot_index, joint_index].item(),
                    'command_x': env.commands[robot_index, 0].item(),
                    'command_y': env.commands[robot_index, 1].item(),
                    'command_yaw': env.commands[robot_index, 2].item(),
                    'base_vel_x': env.base_lin_vel[robot_index, 0].item(),
                    'base_vel_y': env.base_lin_vel[robot_index, 1].item(),
                    'base_vel_z': env.base_lin_vel[robot_index, 2].item(),
                    'base_vel_yaw': env.base_ang_vel[robot_index, 2].item(),
                    'contact_forces_z': env.contact_forces[robot_index, env.feet_indices, 2].cpu().numpy()
                }
            )
        elif i==stop_state_log:
            logger.plot_states()
        if  0 < i < stop_rew_log:
            if infos["episode"]:
                num_episodes = torch.sum(env.reset_buf).item()
                if num_episodes>0:
                    logger.log_rewards(infos["episode"], num_episodes)
        elif i==stop_rew_log:
            logger.print_rewards()
        i+=1
    log_data(env)

if __name__ == '__main__':
    args = get_args()
    play(args)
